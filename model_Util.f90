        SPEC_NAME(i)  = SPEC_NAME(i)(2:)

        do J=NVAR+1,NVAR+NFIX    
         
            if (TRIM(SPEC_NAME(I)) .EQ. TRIM(SPC_NAMES(J))) then 
                FOUND=1
                FIX(J-NVAR)=CONCS(I)
                print*, 'hard-constrained ', spec_name(i), ' at ', concs(I)
                    
                if (SPEC_NAME(I) .EQ. 'EMISS') then
                    i_EMISS = (J-NVAR)        
