    LAT=CONCS(I)

    CASE('LON')  
    LON=CONCS(I)

    CASE('TEMP') 
    TEMP=CONCS(I)

    CASE('JDAY')  
    JDAY=CONCS(I)

    CASE('TIME(GMTs)')  
    continue

    CASE('O3COL') 
    O3COL=CONCS(I)

    CASE('ALBEDO')  
    ALBEDO=CONCS(I)

    CASE('SAREA')  
    SAREA=CONCS(I)

    CASE('RP1')  
    RP1=CONCS(I)

    CASE('JNO2') 
